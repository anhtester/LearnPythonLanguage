import math

x = float(input("X = "))

print("PI = ", math.pi)
print("|X| =  ", math.fabs(x))
print("CanBac2(x) = ", math.sqrt(x))
print("ceil(X) = ", math.ceil(x)) #Giá trị nhỏ nhất mà lớn hơn phần nguyên của X
print("floor(X) = ", math.floor(x)) #Giá trị lớn nhất mà nhỏ hơn phần nguyên của X